#Сравнение величин

nul = 0
num = 0
max = 1
cap = 'A'
low = 'a'

print('Equality :\t', nul, '==', num, nul == num)
print('Equality :\t', cap, '==', low, cap == low)
print('Equality :\t', nul, '!=', num, nul != num)
print('Inequality :\t', nul, '!=', num, nul != max)
print('Greater :\t', nul, '>', max, nul > max)
print('Lesser :\t', nul, '<', max, nul < max)
print('More Or Equal :\t', nul, '>=', max, nul >= max)
print('Less Or Equal :\t', nul, '<=', max, nul <= max)
