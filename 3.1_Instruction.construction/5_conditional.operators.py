# 1. Проинициализировать переменную, содержащую целочисленное значение
# с помощью пользовательского ввода
num = int(input('Please, enter a numdeb: '))
# ! При пользовательском вводе применяется строковый тип, поэтому приводим переменную к целому значению с пом. int()

# 2. Выполнить проверку переменной и вывести результат.
if num > 5 :
    print('Number exceeds 5')
elif num < 5 :
    print('Number is less then 5')
else :
    print('Number is 5')

# 3. Выполенить проверку переменной еще раз, используя два выражения
# вывести результат только в случае успеха.
if num > 7 and num < 9 :
    print('Number is 8')
if num == 1 or num == 3 :
    print('Number Is 1 or 3')