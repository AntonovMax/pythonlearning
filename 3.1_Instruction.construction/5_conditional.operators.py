#       выполняемая-конструкция-если-выражение-ложно
#       выполняемая-конструкция-если-выражение-ложно
#
#   Конструкции if: elif: else: являются эквивалентом инструкций switch или case в других языках
#
#   После блока проверки if может использоваться elif (else if), которое предполагает
# осуществление альтернативной проверки и выполнение соответствующих инструкций.
# Это ключевое слово должно быть привязано к if и завершать его нужно двоеточием, инструкции
# так же отделяются отступами. Затем можно добавить else, на случай, если проверка не пройдена.
#
#   Пример:
#   if проверочное-выражение-1 :
