#   Условный оператор if проверяет условие и возвращает True или False
# пример выражения:
#   if проверочное-выражение :
#       выполняемая-конструкция-если-выражение-истинно
#       выполняемая-конструкция-если-выражение-ложно
#
# с помощью ключевого слова else можно добавить инструкцию, если оба выражения ложны:
#   if проверочное-выражение :
#       выполняемая-конструкция-если-выражение-истинно
#       выполняемая-конструкция-если-выражение-истинно
